    /// <summary>
    /// Expands a cipher key into the ARIA key schedule.
    /// </summary>
    /// <param name="key">The cipher key (16, 24, or 32 bytes).</param>
    /// <param name="encRoundKeys">Output buffer for encryption round keys (at least (nr+1)*16 bytes).</param>
    /// <returns>The number of rounds (12, 14, or 16).</returns>
    [MethodImpl(MethodImplOptionsEx.OptimizedLoop)]
    public static int ExpandKey(ReadOnlySpan<byte> key, Span<byte> encRoundKeys)
    {
        int nr = key.Length switch
        {
            16 => 12,
            24 => 14,
            32 => 16,
            _ => throw new ArgumentException("Key must be 16, 24, or 32 bytes.", nameof(key))
        };

        // Determine C1, C2, C3 indices based on key size
        int c1Idx, c2Idx, c3Idx;
        if (key.Length == 16) { c1Idx = 0; c2Idx = 2; c3Idx = 4; }
        else if (key.Length == 24) { c1Idx = 2; c2Idx = 4; c3Idx = 0; }
        else { c1Idx = 4; c2Idx = 0; c3Idx = 2; }

        // KL = leftmost 128 bits of key, KR = rightmost bits (zero-padded)
        Span<byte> kl = stackalloc byte[16];
        Span<byte> kr = stackalloc byte[16];
        key.Slice(0, 16).CopyTo(kl);
        if (key.Length > 16)
            key.Slice(16, Math.Min(key.Length - 16, 16)).CopyTo(kr);

        // W0 = KL
        Span<byte> w0 = stackalloc byte[16];
        Span<byte> w1 = stackalloc byte[16];
        Span<byte> w2 = stackalloc byte[16];
        Span<byte> w3 = stackalloc byte[16];
        kl.CopyTo(w0);

        // W1 = FO(W0, C1) ^ KR
        Span<byte> temp = stackalloc byte[16];
        FO(w0, C[c1Idx], C[c1Idx + 1], temp);
        Xor128(temp, kr, w1);

        // W2 = FE(W1, C2) ^ W0
        FE(w1, C[c2Idx], C[c2Idx + 1], temp);
        Xor128(temp, w0, w2);

        // W3 = FO(W2, C3) ^ W1
        FO(w2, C[c3Idx], C[c3Idx + 1], temp);
        Xor128(temp, w1, w3);

        // Generate encryption round keys from W0-W3 using rotations
        // ek1  = W0 ^ (W1 >>> 19)
        // ek2  = W1 ^ (W2 >>> 19)  ... etc
        GenerateRoundKeys(w0, w1, w2, w3, encRoundKeys, nr);

        return nr;
    }

    /// <summary>
    /// Creates decryption round keys from encryption round keys.
    /// </summary>
    /// <param name="encRoundKeys">Encryption round keys.</param>
    /// <param name="decRoundKeys">Output decryption round keys.</param>
    /// <param name="nr">Number of rounds.</param>
    public static void CreateDecryptionKeys(ReadOnlySpan<byte> encRoundKeys, Span<byte> decRoundKeys, int nr)
    {
        // dk[0] = ek[nr] (last enc key)
        encRoundKeys.Slice(nr * 16, 16).CopyTo(decRoundKeys);

        // dk[i] = A(ek[nr-i]) for 1 <= i < nr
        for (int i = 1; i < nr; i++)
        {
            DiffuseA(encRoundKeys.Slice((nr - i) * 16, 16), decRoundKeys.Slice(i * 16, 16));
        }

        // dk[nr] = ek[0] (first enc key)
        encRoundKeys.Slice(0, 16).CopyTo(decRoundKeys.Slice(nr * 16, 16));
    }

    /// <summary>
    /// Encrypts a single 16-byte block using ARIA.
    /// </summary>
    [MethodImpl(MethodImplOptionsEx.OptimizedLoop)]
    public static void EncryptBlock(ReadOnlySpan<byte> input, Span<byte> output, ReadOnlySpan<byte> roundKeys, int nr)
    {
        Span<byte> state = stackalloc byte[16];
        Span<byte> temp = stackalloc byte[16];
        input.Slice(0, 16).CopyTo(state);

        for (int r = 1; r <= nr; r++)
        {
            // XOR round key
            for (int i = 0; i < 16; i++)
                state[i] ^= roundKeys[(r - 1) * 16 + i];

            // Substitution layer
            if (r % 2 == 1)
                SubstLayerOdd(state);
            else
                SubstLayerEven(state);

            // Diffusion layer (not on final round)
            if (r < nr)
            {
                DiffuseA(state, temp);
                temp.CopyTo(state);
            }
        }

        // XOR final round key
        for (int i = 0; i < 16; i++)
            output[i] = (byte)(state[i] ^ roundKeys[nr * 16 + i]);
    }

    /// <summary>
    /// Decrypts a single 16-byte block using ARIA.
    /// </summary>
    [MethodImpl(MethodImplOptionsEx.OptimizedLoop)]
    public static void DecryptBlock(ReadOnlySpan<byte> input, Span<byte> output, ReadOnlySpan<byte> roundKeys, int nr)
    {
        // Decryption uses the same structure as encryption with decryption round keys
        EncryptBlock(input, output, roundKeys, nr);
    }

    // Odd-round substitution: SB1, SB2, SB3, SB4 pattern
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static void SubstLayerOdd(Span<byte> x)
    {
        x[0]  = SB1[x[0]];  x[1]  = SB2[x[1]];  x[2]  = SB3[x[2]];  x[3]  = SB4[x[3]];
        x[4]  = SB1[x[4]];  x[5]  = SB2[x[5]];  x[6]  = SB3[x[6]];  x[7]  = SB4[x[7]];
        x[8]  = SB1[x[8]];  x[9]  = SB2[x[9]];  x[10] = SB3[x[10]]; x[11] = SB4[x[11]];
        x[12] = SB1[x[12]]; x[13] = SB2[x[13]]; x[14] = SB3[x[14]]; x[15] = SB4[x[15]];
    }

    // Even-round substitution: SB3, SB4, SB1, SB2 pattern
    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static void SubstLayerEven(Span<byte> x)
    {
        x[0]  = SB3[x[0]];  x[1]  = SB4[x[1]];  x[2]  = SB1[x[2]];  x[3]  = SB2[x[3]];
        x[4]  = SB3[x[4]];  x[5]  = SB4[x[5]];  x[6]  = SB1[x[6]];  x[7]  = SB2[x[7]];
        x[8]  = SB3[x[8]];  x[9]  = SB4[x[9]];  x[10] = SB1[x[10]]; x[11] = SB2[x[11]];
        x[12] = SB3[x[12]]; x[13] = SB4[x[13]]; x[14] = SB1[x[14]]; x[15] = SB2[x[15]];
    }

    // Diffusion layer A (binary matrix from RFC 5794)
    private static void DiffuseA(ReadOnlySpan<byte> x, Span<byte> y)
    {
        y[0]  = (byte)(x[3] ^ x[4] ^ x[6] ^ x[8]  ^ x[9]  ^ x[13] ^ x[14]);
        y[1]  = (byte)(x[2] ^ x[5] ^ x[7] ^ x[8]  ^ x[9]  ^ x[12] ^ x[15]);
        y[2]  = (byte)(x[1] ^ x[4] ^ x[6] ^ x[10] ^ x[11] ^ x[12] ^ x[15]);
        y[3]  = (byte)(x[0] ^ x[5] ^ x[7] ^ x[10] ^ x[11] ^ x[13] ^ x[14]);
        y[4]  = (byte)(x[0] ^ x[2] ^ x[5] ^ x[8]  ^ x[11] ^ x[14] ^ x[15]);
        y[5]  = (byte)(x[1] ^ x[3] ^ x[4] ^ x[9]  ^ x[10] ^ x[14] ^ x[15]);
        y[6]  = (byte)(x[0] ^ x[2] ^ x[7] ^ x[9]  ^ x[10] ^ x[12] ^ x[13]);
        y[7]  = (byte)(x[1] ^ x[3] ^ x[6] ^ x[8]  ^ x[11] ^ x[12] ^ x[13]);
        y[8]  = (byte)(x[0] ^ x[1] ^ x[4] ^ x[7]  ^ x[10] ^ x[13] ^ x[15]);
        y[9]  = (byte)(x[0] ^ x[1] ^ x[5] ^ x[6]  ^ x[11] ^ x[12] ^ x[14]);
        y[10] = (byte)(x[2] ^ x[3] ^ x[5] ^ x[6]  ^ x[8]  ^ x[13] ^ x[15]);
        y[11] = (byte)(x[2] ^ x[3] ^ x[4] ^ x[7]  ^ x[9]  ^ x[12] ^ x[14]);
        y[12] = (byte)(x[1] ^ x[2] ^ x[6] ^ x[7]  ^ x[9]  ^ x[11] ^ x[12]);
        y[13] = (byte)(x[0] ^ x[3] ^ x[6] ^ x[7]  ^ x[8]  ^ x[10] ^ x[13]);
        y[14] = (byte)(x[0] ^ x[3] ^ x[4] ^ x[5]  ^ x[9]  ^ x[11] ^ x[14]);
        y[15] = (byte)(x[1] ^ x[2] ^ x[4] ^ x[5]  ^ x[8]  ^ x[10] ^ x[15]);
    }

    // FO: Odd round function (substitution layer 1 + diffusion)
    private static void FO(ReadOnlySpan<byte> d, ulong ckHi, ulong ckLo, Span<byte> output)
    {
        Span<byte> temp = stackalloc byte[16];
        // XOR with round constant
        ulong hi = BinaryPrimitives.ReadUInt64BigEndian(d) ^ ckHi;
        ulong lo = BinaryPrimitives.ReadUInt64BigEndian(d.Slice(8)) ^ ckLo;
        BinaryPrimitives.WriteUInt64BigEndian(temp, hi);
        BinaryPrimitives.WriteUInt64BigEndian(temp.Slice(8), lo);

        SubstLayerOdd(temp);
        DiffuseA(temp, output);
    }

    // FE: Even round function (substitution layer 2 + diffusion)
    private static void FE(ReadOnlySpan<byte> d, ulong ckHi, ulong ckLo, Span<byte> output)
    {
        Span<byte> temp = stackalloc byte[16];
        ulong hi = BinaryPrimitives.ReadUInt64BigEndian(d) ^ ckHi;
        ulong lo = BinaryPrimitives.ReadUInt64BigEndian(d.Slice(8)) ^ ckLo;
        BinaryPrimitives.WriteUInt64BigEndian(temp, hi);
        BinaryPrimitives.WriteUInt64BigEndian(temp.Slice(8), lo);

        SubstLayerEven(temp);
        DiffuseA(temp, output);
    }

    [MethodImpl(MethodImplOptions.AggressiveInlining)]
    private static void Xor128(ReadOnlySpan<byte> a, ReadOnlySpan<byte> b, Span<byte> result)
    {
        for (int i = 0; i < 16; i++)
            result[i] = (byte)(a[i] ^ b[i]);
    }

    // Rotate 128-bit value right by n bits
    private static void RotateRight128(ReadOnlySpan<byte> input, int n, Span<byte> output)
    {
        int byteShift = n / 8;
        int bitShift = n % 8;

        for (int i = 0; i < 16; i++)
        {
            int srcIdx = (i - byteShift + 16) % 16;
            int prevIdx = (srcIdx - 1 + 16) % 16;

            if (bitShift == 0)
            {
                output[i] = input[srcIdx];
            }
            else
            {
                output[i] = (byte)((input[srcIdx] >> bitShift) | (input[prevIdx] << (8 - bitShift)));
            }
        }
    }

    // Rotate 128-bit value left by n bits
    private static void RotateLeft128(ReadOnlySpan<byte> input, int n, Span<byte> output)
    {
        RotateRight128(input, 128 - n, output);
    }

    private static void GenerateRoundKeys(ReadOnlySpan<byte> w0, ReadOnlySpan<byte> w1,
        ReadOnlySpan<byte> w2, ReadOnlySpan<byte> w3, Span<byte> ek, int nr)
    {
        Span<byte> rot = stackalloc byte[16];

        // Copy W values to stackalloc buffers for indexed access
        Span<byte> wBuf = stackalloc byte[64];
        w0.CopyTo(wBuf.Slice(0, 16));
        w1.CopyTo(wBuf.Slice(16, 16));
        w2.CopyTo(wBuf.Slice(32, 16));
        w3.CopyTo(wBuf.Slice(48, 16));

        for (int i = 0; i <= nr; i++)
        {
            int wBase = (i % 4) * 16;
            int wRot = ((i % 4 + 1) % 4) * 16;

            int rotAmount;
            bool isLeft;
            if (i < 4) { rotAmount = 19; isLeft = false; }
            else if (i < 8) { rotAmount = 31; isLeft = false; }
            else if (i < 12) { rotAmount = 61; isLeft = true; }
            else if (i < 16) { rotAmount = 31; isLeft = true; }
            else { rotAmount = 19; isLeft = true; }

            if (isLeft)
                RotateLeft128(wBuf.Slice(wRot, 16), rotAmount, rot);
            else
                RotateRight128(wBuf.Slice(wRot, 16), rotAmount, rot);

            Xor128(wBuf.Slice(wBase, 16), rot, ek.Slice(i * 16, 16));
        }
    }
